package example01;

public class Example03 {
 public static void run() {
	 //switch문(알람시스템)
	 
	 int time=6;
	 switch(time) {
	 case 6:
		 System.out.println("기상");
		 break;
		 
	 case 8:
		 System.out.println("출근");
		 break;
		 
	 case 12:
		 System.out.println("점심");
		 break;
		 
	 case 18:
		 System.out.println("퇴근");
		 break;
		 
	 case 19:
		 System.out.println("저녁");
		 break;
		 
	 case 21:
		 System.out.println("운동");
		 break;
		 
	 case 23:
		 System.out.println("취침");
		 break;
	 }
 }
}
